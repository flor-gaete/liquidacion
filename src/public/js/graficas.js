export const restaurarCanvas = () => {
    const chart = document.querySelector('#myChart');
    const cardBody = document.querySelector('#papa-de-canvas');
    cardBody.removeChild(chart);

    let canvasNuevo = document.createElement('canvas');
    canvasNuevo.setAttribute('id', 'myChart');
    cardBody.appendChild(canvasNuevo);
}

export const graficoBarraV = (datos) => {
    let dataLabels = Object.keys(datos);
    let dataValues = Object.values(datos);
    // Line
    var ctx = document.getElementById("myChart").getContext('2d');
    var myChart = new Chart(ctx, {
        type: 'bar',
        data: {
            labels: dataLabels,
            datasets: [{
                label: '# of Votes',
                data: dataValues,
                backgroundColor: [
                    'rgba(255, 99, 132, 0.2)',
                    'rgba(54, 162, 235, 0.2)',
                    'rgba(255, 206, 86, 0.2)',
                    'rgba(75, 192, 192, 0.2)',
                    'rgba(153, 102, 255, 0.2)',
                    'rgba(255, 159, 64, 0.2)'
                ],
                borderColor: [
                    'rgba(255,99,132,1)',
                    'rgba(54, 162, 235, 1)',
                    'rgba(255, 206, 86, 1)',
                    'rgba(75, 192, 192, 1)',
                    'rgba(153, 102, 255, 1)',
                    'rgba(255, 159, 64, 1)'
                ],
                borderWidth: 1
            }]
        },
        options: {
            scales: {
                yAxes: [{
                    ticks: {
                        beginAtZero: true
                    }
                }]
            },
            maintainAspectRatio: false,
        }
    });

};

